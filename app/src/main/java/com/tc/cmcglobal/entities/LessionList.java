public class LessionList {
     public int system_id;
     public String lesson_cd;
     public String lesson_nm;
     public String date;
     public String start_time;
     public String end_time;
     public int class_id;
     public String class_cd;
     public String class_nm;
     public String teacher_id;
     public String teacher_cd;
     public String teacher_name;

     public LessionList(int system_id,
                        String lesson_cd,
                        String lesson_nm,
                        String date,
                        String start_time,
                        String end_time,
                        int class_id,
                        String class_cd,
                        String class_nm,
                        String teacher_id,
                        String teacher_cd,
                        String teacher_name) {
          this.system_id = system_id;
          this.lesson_cd = lesson_cd;
          this.lesson_nm = lesson_nm;
          this.date = date;
          this.start_time = start_time;
          this.end_time = end_time;
          this.class_id = class_id;
          this.class_cd = class_cd;
          this.class_nm = class_nm;
          this.teacher_id = teacher_id;
          this.teacher_cd = teacher_cd;
          this.teacher_name = teacher_name;
     }

     public int getSystem_id() {
          return system_id;
     }

     public void setSystem_id(int system_id) {
          this.system_id = system_id;
     }

     public String getLesson_cd() {
          return lesson_cd;
     }

     public void setLesson_cd(String lesson_cd) {
          this.lesson_cd = lesson_cd;
     }

     public String getLesson_nm() {
          return lesson_nm;
     }

     public void setLesson_nm(String lesson_nm) {
          this.lesson_nm = lesson_nm;
     }

     public String getDate() {
          return date;
     }

     public void setDate(String date) {
          this.date = date;
     }

     public String getStart_time() {
          return start_time;
     }

     public void setStart_time(String start_time) {
          this.start_time = start_time;
     }

     public String getEnd_time() {
          return end_time;
     }

     public void setEnd_time(String end_time) {
          this.end_time = end_time;
     }

     public int getClass_id() {
          return class_id;
     }

     public void setClass_id(int class_id) {
          this.class_id = class_id;
     }

     public String getClass_cd() {
          return class_cd;
     }

     public void setClass_cd(String class_cd) {
          this.class_cd = class_cd;
     }

     public String getClass_nm() {
          return class_nm;
     }

     public void setClass_nm(String class_nm) {
          this.class_nm = class_nm;
     }

     public String getTeacher_id() {
          return teacher_id;
     }

     public void setTeacher_id(String teacher_id) {
          this.teacher_id = teacher_id;
     }

     public String getTeacher_cd() {
          return teacher_cd;
     }

     public void setTeacher_cd(String teacher_cd) {
          this.teacher_cd = teacher_cd;
     }

     public String getTeacher_name() {
          return teacher_name;
     }

     public void setTeacher_name(String teacher_name) {
          this.teacher_name = teacher_name;
     }
}
